
class Event{

  final String title;
  final String description;
  final String venue;
  final String city;
  final DateTime dateTime;
  final String imageUrl;
  final String price;

  Event({
    required this.title,
    required this.description,
    required this.venue,
    required this.city,
    required this.dateTime,
    required this.imageUrl,
    required this.price,
  });
}


